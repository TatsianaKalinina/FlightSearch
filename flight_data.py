class FlightData:

    def __init__(self, fly_from_city, fly_to_city, fly_from_airport, fly_to_airport, date_from, date_to, price):
        self.fly_from = fly_from_city
        self.fly_from_airport = fly_to_airport
        self.fly_to = fly_to_city
        self.fly_to_airport = fly_to_airport
